pub(crate) fn latex_escape(ch: char) -> Option<&'static str> {
    match ch {
        '#' => Some(r"\#"),
        '$' => Some(r"\$"),
        '%' => Some(r"\%"),
        '&' => Some(r"\&"),
        '@' => Some(r"\@"),
        '*' => Some(r"\textasteriskcentered"),
        '_' => Some(r"\_"),
        '|' => Some(r"\vert{}"),
        '\u{00a0}' => Some("~"),
        'À' => Some(r"\`{A}"),
        'Á' => Some(r"\'{A}"),
        'Â' => Some(r"\^{A}"),
        'Ã' => Some(r"\~{A}"),
        'Ä' => Some(r#"\"{A}"#),
        'Å' => Some(r"\AA{}"),
        'Æ' => Some(r"\AE{}"),
        'Ç' => Some(r"\c{C}"),
        'È' => Some(r"\`{E}"),
        'É' => Some(r"\'{E}"),
        'Ê' => Some(r"\^{E}"),
        'Ë' => Some(r#"\"{E}"#),
        'Ì' => Some(r"\`{I}"),
        'Í' => Some(r"\'{I}"),
        'Î' => Some(r"\^{I}"),
        'Ï' => Some(r#"\"{I}"#),
        'Ð' => Some(r"\DH{}"),
        'Ñ' => Some(r"\~{N}"),
        'Ò' => Some(r"\`{O}"),
        'Ó' => Some(r"\'{O}"),
        'Ô' => Some(r"\^{O}"),
        'Õ' => Some(r"\~{O}"),
        'Ö' => Some(r#"\"{O}"#),
        'Ø' => Some(r"\O{}"),
        'Ù' => Some(r"\`{U}"),
        'Ú' => Some(r"\'{U}"),
        'Û' => Some(r"\^{U}"),
        'Ü' => Some(r#"\"{U}"#),
        'Ý' => Some(r"\'{Y}"),
        'Þ' => Some(r"\TH{}"),
        'ß' => Some(r"\ss{}"),
        'à' => Some(r"\`{a}"),
        'á' => Some(r"\'{a}"),
        'â' => Some(r"\^{a}"),
        'ã' => Some(r"\~{a}"),
        'ä' => Some(r#"\"{a}"#),
        'å' => Some(r"\aa{}"),
        'æ' => Some(r"\ae{}"),
        'ç' => Some(r"\c{c}"),
        'è' => Some(r"\`{e}"),
        'é' => Some(r"\'{e}"),
        'ê' => Some(r"\^{e}"),
        'ë' => Some(r#"\"{e}"#),
        'ì' => Some(r"\`{\i}"),
        'í' => Some(r"\'{\i}"),
        'î' => Some(r"\^{\i}"),
        'ï' => Some(r#"\"{\i}"#),
        'ð' => Some(r"\dh{}"),
        'ñ' => Some(r"\~{n}"),
        'ò' => Some(r"\`{o}"),
        'ó' => Some(r"\'{o}"),
        'ô' => Some(r"\^{o}"),
        'õ' => Some(r"\~{o}"),
        'ö' => Some(r#"\"{o}"#),
        'ø' => Some(r"\o{}"),
        'ù' => Some(r"\`{u}"),
        'ú' => Some(r"\'{u}"),
        'û' => Some(r"\^{u}"),
        'ü' => Some(r#"\"{u}"#),
        'ý' => Some(r"\'{y}"),
        'þ' => Some(r"\th{}"),
        'ÿ' => Some(r#"\"{y}"#),
        'ū' => Some(r"\={u}"),
        'ɛ' => Some(r"\varepsilon{}"),
        _ => None,
    }
}
